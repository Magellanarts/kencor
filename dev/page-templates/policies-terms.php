          <h3>What is the HITECH Act and the Final HIPAA Omnibus rule?</h3>
          <p>The Health Information Technology for Economic and Clinical Health (HITECH) Act   was signed into law in 2009, to promote the adoption and meaningful use of health information technology in the U.S.</p>

          <p>In 2013, the final HIPAA Omnibus rule set further statutory requirements, which greatly enhanced a patient’s privacy rights and protections, including holding all custodians of Protected Health Information (PHI) — including HIPAA Business Associates (BA) — subject to the same security and privacy rules as Covered Entities under HIPAA.</p>

          <h3>What is a HIPAA Business Associate Agreement (BAA)?</h3>
